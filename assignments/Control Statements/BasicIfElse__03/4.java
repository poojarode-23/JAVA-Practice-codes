class Basicif4{
	public static void main(String[] args){
		char ch1 = 'a';
		char ch2 = 'A'; 
		if(ch1>=97 && ch1<=122){
			System.out.println(ch1 +"is a lowercase");
		}
		else{
			System.out.println(ch1 +"is a UPPERCASE");
		}
		if(ch2>=65 && ch2<=90){
			System.out.println(ch2 +  "is a UPPERCASE");
		}
		else{
			System.out.println(ch2 + "is lowercase");
		}

	}
}
