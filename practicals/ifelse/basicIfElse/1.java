class Basicif1{
	public static void main(String[] args){
		int num = 5;
		if(num>0){
			System.out.println(num+"is a positive");
		}
		else{
			System.out.println(num+"is negative");
		}
		int num1 = -5;
		if(num1<0){
			System.out.println(num1+  "is a negative");
		}
		else{
			System.out.println(num1+ "is positive");
		}

	}
}
